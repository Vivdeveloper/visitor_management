export const VISITOR_LANGS = [
  { code: "en", label: "English" },
  { code: "hi", label: "हिन्दी" },
  { code: "mr", label: "मराठी" },
] as const;

export type VisitorLang = (typeof VISITOR_LANGS)[number]["code"];

const STORAGE_KEY = "vms_visitor_lang";

export function getStoredVisitorLang(): VisitorLang {
  try {
    const v = sessionStorage.getItem(STORAGE_KEY);
    if (v === "en" || v === "hi" || v === "mr") return v;
  } catch {
    /* ignore */
  }
  return "en";
}

export function setStoredVisitorLang(lang: VisitorLang) {
  try {
    sessionStorage.setItem(STORAGE_KEY, lang);
  } catch {
    /* ignore */
  }
}

