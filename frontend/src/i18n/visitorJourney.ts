const TABLES: Record<VisitorLang, Dict> = { en, hi, mr };

export type VisitorCopyKey = keyof typeof en;

export function vt(lang: VisitorLang, key: VisitorCopyKey, vars?: Record<string, string>): string {
  const raw = TABLES[lang]?.[key] || TABLES.en[key] || key;
  if (!vars) return raw;
  return Object.entries(vars).reduce((s, [k, v]) => s.replace(`{${k}}`, v), raw);
}
